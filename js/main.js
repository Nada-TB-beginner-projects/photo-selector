'use strict';

/*************************************************************************************************/
/* ****************************************** DONNEES ****************************************** */
/*************************************************************************************************/
var photos;
var text;
photos= document.querySelectorAll("#photo-list li");
text= document.querySelector("#total span");



/*************************************************************************************************/
/* ***************************************** FONCTIONS ***************************************** */
/*************************************************************************************************/
function Change(){
	var photoSelected;
	this.classList.toggle("selected");
	photoSelected =document.querySelectorAll("#photo-list li.selected");		
	text.textContent = photoSelected.length;
}


/*************************************************************************************************/
/* ************************************** CODE PRINCIPAL *************************************** */
/*************************************************************************************************/
for (var i = 0; i < photos.length; i++) {
	photos[i].addEventListener('click', Change);
}



